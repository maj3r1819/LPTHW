def sum(a,b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b

def doublemul(a,b):
    return a**b

def doublediv(a,b):
    return a//b

def mod(a,b):
    return a%b


